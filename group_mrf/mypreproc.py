#!/usr/bin/env python 
import os
import re
import subprocess
import optparse

def main():
    usage = "usage: %prog [options]"
    parser = optParse.OptionParser(usage)
    parser.add_option("-i", "--indir", dest="indir",
                       action="store", type="string",
                       default=".",
                       help="input directory.")
    parser.add_option("-o", "--outdir", dst="outdir",
                      action="store", type="string",
                      default=".",
                      help="output dir.")
    parser.add_option("-v", "--verbose",
                      acion="store_true", dest="verbose", default=True,
                      help="Keep a lot intermediate files.")

    (options, args) = parser.parse_args()

    if options.indir:
        subprocess.call("ls -l", shell=True)

    if options.outdir:
        subprocess.call("ls -l", shell=True)

        

        

        

        

        
        
    
